#include "WS51F6240.h"						
/*****************************************************************************************
*函数名称：void check_sysclo(void)
*函数功能：
*入口参数：				 
*出口参数：
*****************************************************************************************/
void check_sysclo(void)
{
	PWM0CFG = 0X00;
	PWM0DUTL=10;
	PWM0DUTH=0;
	PWM0DIVL=0;
	PWM0DIVH=0;	
	PWM0PS = 0;	
}

/**************************************************************
说明：校准系统时钟
***************************************************************/

void main()
{
	  //系统时钟配置
		SCCON = 0X00;
		HRCON |= 0x80;
	
			/******************* 管脚配置 ******************/
		P00F=0X03;	//PWM0	
			
		/****************** PWM0/1组 ********************/
		check_sysclo();
		
		PWMRUN=0x01;	//使能 PWM 运行模块
		
		while (1);
	
}