    /* set timer1 as mode0 (13-bit) */
    TL1 = (8192-1334) & 0x1F;       //初始值低字节，TL0 的高 3 位是无效的
    TH1 = (8192-1334) >> 5;         //初始值高字节(1ms@16MHz)
	
	/* 工作模式1：16 位定时器 */
